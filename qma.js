        +"分成含有熱量的三大營養素分別是碳水化合物、蛋白質、脂肪，以及不含有熱量的維生素、礦物質、水。\n"
        +"最重要的是要吃得乾淨。\n"
        +"吃得乾淨就是不能有太多的醬料、添加物還有避免加工食品，\n"
